							ARTICLE I - NAME</span><br /><br />

							The name of this organization shall be the Greater Spokane Chapter, hereafter referred to as the "Chapter" of the Information Systems Security Association, Inc., hereafter referred to as the "Association".<br /><br />

							ARTICLE II - PURPOSE AND OBJECTIVES<br /><br />

							The primary purpose of the Chapter is to promote the education of its members for the improvement and development of their capabilities relating to the security of the information systems processing.<br /><br />

							More specifically, the objectives of the Association are (a) to promote the education of, and help expand the knowledge and skills of its members in the interrelated fields of information systems security, and information or data processing; (b) to encourage a free exchange of information security techniques, approaches, and problem solving by its members; (c) to provide adequate communication to keep members abreast of current events in information processing and security which can be beneficial to them and employers; and (d) to communicate to management, and to systems and information  processing professionals the importance of establishing controls necessary to ensure the secure organization and utilization of information processing resources.<br /><br />

							ARTICLE III - MEMBERSHIP<br /><br />

							Section 1.&nbsp;&nbsp;Membership in the Chapter is based upon one having primary interest and active involvement in information systems security in the private or public sector.  In addition, membership is contingent upon interest in the purposes and objectives of the chapter as stated in Article II, acceptance of the Association Code of Ethics and is subject to provisions of Articles of Incorporation and Bylaws and the types of membership established by the Association Board of Directors.

							The types of membership may be referenced at the Association's website, www.issa.org.<br /><br />

							Section 2.&nbsp;&nbsp;Other - The Chapter Officers with the approval of the Association's Vice President of Membership may provide for other types of membership.<br /><br />

							Section 3.&nbsp;&nbsp;Members in Good Standing - Members who maintain their membership by payment of dues as required under the Article VII of the Bylaws and who otherwise qualify shall be considered in good standing and entitled to full privilege of membership.<br /><br />

							Section 4.&nbsp;&nbsp;Resignation - any member may resign at any time, but such resignation shall not relieve the resigning individual from payment of dues for the expired portion of the current fiscal year or give any right to rebate of dues or any right to a pro rata or other share of the assets of the Chapter.  All resignations shall be made in writing.<br /><br />

							Section 5.&nbsp;&nbsp;Expulsion - The Officers, at any meeting at which a quorum is present may, by a two-thirds vote of those present terminate the membership of any member who in its judgment has violated the Bylaws, Code of Ethics, or who has been guilty of conduct detrimental to the interests of the Chapter, provided that such person shall have been granted an opportunity for a hearing before the Officers.  The Officers shall cause at least thirty days (30) notice of the hearing to be given in writing, delivered by registered mail, to the member against whom charges may be preferred.  Such action by the Officers shall be final and shall cancel all rights, interests or privileges of such member in the services or resources of the Chapter.  The presiding Officer shall have the authority to appoint a Sergeant At Arms to preserve order and execute commands.<br /><br />

							Section 6.&nbsp;&nbsp;Termination - Membership may be terminated if payment of the annual Chapter dues has not been received by the Chapter Treasurer as provided for in Article VII.<br /><br />
							 
							ARTICLE IV - OFFICERS<br /><br />

							Section 1.&nbsp;&nbsp;The officers of the Chapter must be general members in good standing as of the date of their election.  The officers shall consist, at minimum, of President, Vice President, Secretary, and Treasurer.<br /><br />

							Section 2.&nbsp;&nbsp;The President shall preside at all meetings of the Chapter and shall obey the mandates of the members.  The President shall have the power to call special meetings if deemed necessary for the benefit of the Chapter and shall have the deciding vote in case of tied decisions.<br /><br />

							Section 3.&nbsp;&nbsp;The Vice President shall attend to the duties of the President in his/her absence or in case the President's office may become vacant for any cause whatever, and shall attend to any other duties as the President may require.  Additionally, the Vice President shall review all membership applications for eligibility.<br /><br />

							Section 4.&nbsp;&nbsp;The Secretary shall record and keep minutes of all meetings.  The Secretary shall perform a bank reconciliation monthly.  The Secretary shall maintain sufficient membership address lists as to ensure that all members in good standing are notified of meetings, and that the members receive all other correspondence necessary to the conduct of the Chapter.  At the direction of the President, the Secretary shall also transmit and respond to all correspondence of the Chapter.<br /><br />

							Section 5.&nbsp;&nbsp;The Treasurer shall collect all membership dues and other monies or articles of value belonging to the Chapter, and shall keep an accurate account of all treasury receipts, expenditures, and deposits.<br /><br />

							Section 6.&nbsp;&nbsp;All past Presidents retaining active membership shall be privileged to attend such meetings held by the Officers, to act only in an advisory capacity and without power of vote.<br /><br />

							Section 7.&nbsp;&nbsp;The business of the Chapter shall be managed by the Officers.  A quorum for business shall consist of three officers present.  An officer quorum may, from time to time, establish special committees for various purposes as required.<br /><br />

							Section 8.&nbsp;&nbsp;In case of an officer vacancy other than the Office of President, such vacancy shall be filled by appointment by the President, upon the advice of the remaining officers, and subject to the consent of a majority of the membership attending the next general meeting.<br /><br />

							Section 9.&nbsp;&nbsp;On a motion and second from the membership at a general meeting, an officer shall be held before the Chapter for malfeasance of duty.  A two-thirds majority of all Chapter members shall be required for removal from office.<br /><br />

							Section 10.&nbsp;&nbsp;A quorum of the Officers, at their discretion, may appoint volunteer members to assist the Officers in running the chapter business such as committee chairs as needed.<br /><br />
							 
							ARTICLE V - ELECTIONS<br /><br />

							Section 1.&nbsp;&nbsp;The Officers shall be elected by popular vote, each general member in good standing to be entitled to one vote.<br /><br />

							Section 2.&nbsp;&nbsp;The Nominating Committee shall consist of two members in good standing as selected by the Officers at the October meeting of each year.  Members in good standing may volunteer for this function.<br /><br />

							Section 3.&nbsp;&nbsp;Elections shall be held during the December meeting as determined by term. (See Section 6 below).<br /><br />

							Section 4.&nbsp;&nbsp;The Nominating Committee Chairman shall prepare and distribute election ballots at least one month prior to the December meeting.<br /><br />

							Section 5.&nbsp;&nbsp;Election results shall be announced at the end of the December meeting. Or at the beginning of the next meeting (time to count ballots if chapter is big)<br /><br />
							
							Section 6.&nbsp;&nbsp;The term of office shall consist of two years commencing at the conclusion of the December meeting.<br /><br />

							ARTICLE VI - MEETINGS</span><br /><br />

							Section 1.</span>&nbsp;&nbsp;The regular meeting of the Chapter shall be held Bi-Monthly<br /><br />

							Section 2.</span>&nbsp;&nbsp;Special meetings may be called by the officers at any time upon ten-day written notice to all Chapter members.<br /><br />

							Section 3.</span>&nbsp;&nbsp;At all meetings, a minimum of ten (10) members in attendance shall constitute a quorum for the transaction of business.<br /><br /> 

							Section 4.</span>&nbsp;&nbsp;The order of business at regular meetings shall be as follows:<br /><br />

							<p class="order">
							a)	Call to Order<br />
							b)	Reading and Approval of Minutes of Last Meeting<br />
							c)	Reading of Minutes of the Officers Meeting<br />
							d)	Report of the Treasurer<br />
							e)	Reports of Special Committees<br />
							f)	Unfinished Business<br />
							g)	New Business<br />
							h)	Special Announcements<br />
							i)	Program Agenda<br />
							j)	Adjournment<br /><br />
							</p>

							Section 5.</span>&nbsp;&nbsp;The order of business may be revised or dispensed with by the officer presiding upon the approval of the members present, if circumstances decree that such action be taken.<br /><br />

							ARTICLE VII - FINANCIAL ADMINISTRATION</span><br /><br />

							Section 1.</span>&nbsp;&nbsp;Annual dues shall be due and payable to the Association by member's renewal date.  Additional Chapter fees may be required as the Officers direct, with the approval of a majority of Chapter members in good standing.  Of the dues collected from each member, the Association will forward Chapter dues to the Chapter Treasurer and will be retained in the Chapter treasury.<br /><br />

							Section 2.</span>&nbsp;&nbsp;Bank accounts in the name of the Chapter shall be established and maintained as directed by the Officers.<br /><br />

							Section 3.</span>&nbsp;&nbsp;Signatory authority for all accounts, which may be established, shall reside in the duly elected President, Vice President and Treasurer. (Suggested that the individual who is responsible for "auditing" the books yearly - does not have signatory authority)<br /><br />

							Section 4.</span>&nbsp;&nbsp;The Secretary shall be responsible for reconciliation of all bank accounts for verification purposes and be responsible for the annual audit of books.  The Secretary shall not have signatory authority.<br /><br />

							Section 5.</span>&nbsp;&nbsp;An Auditing Committee consisting of two members in good standing and/or a qualified accountant shall be appointed by the President at the October meeting of each year.  These individuals shall not be Officers.  The responsibility of the Auditing Committee shall be to examine all financial records of the Chapter and provide a report of its findings and recommendations to the membership at the December meeting prior to elections.  This report shall be in writing, and shall be maintained as part of the permanent records of the Chapter.<br /><br />

							ARTICLE VIII - LIMITATIONS OF LIABILITY</span><br /><br />

							Section 1.</span>&nbsp;&nbsp;Chapter Liability

							The Chapter shall be fully and solely responsible for its own legal and financial affairs, and shall hold harmless the Association by reason of their affiliation, from any lawsuits, damages, other expenses or liabilities arising out of the activities of the Chapter.<br /><br />

							Section 2.</span>&nbsp;&nbsp;ISSA, Inc. Liability

							The Chapter shall not be responsible, nor liable, for any lawsuits, damages, other expenses or liabilities arising out of the activities of the Association.<br /><br />

							ARTICLE IX - HEADQUARTERS</span><br /><br />

							Section 1.</span>&nbsp;&nbsp;The Headquarters of the Greater Spokane Chapter shall be located in the State of  Washington, City of Spokane at the address designated by the Officers.<br /><br />

							ARTICLE X - AMENDMENTS TO THE BYLAWS</span><br /><br />

							Section 1.</span>&nbsp;&nbsp;These Bylaws may be amended, repealed, or added to in the following manner only:<br /><br />
<p class="order">
							a. 	Ten percent of the members of the Chapter or at least two Officers may at any time propose in writing, signed by them and addressed to the Secretary, the amendment or repeal of any existing provision of, or the addition of any new provision to the Bylaws.<br /><br />

							b. 	The Secretary shall present such proposal amendment, repeal, or addition at the next regular meeting of the Officers, and shall incorporate in the notice of that meeting a statement that such proposed amendment, repeal, or addition will be considered.  No such proposed amendment, repeal, or addition shall be considered at any meeting of the Officers unless such notice has been given to each officer not less than 10 calendar days prior to the meeting.<br /><br />

							c. 	At the Officer meeting called in accordance with the provisions of Paragraph b. above, the proposed amendment, repeal, or addition to the Bylaws shall be considered and voted upon the Officers present.  If, at the meeting a quorum being present, a majority of the total number of Officers present vote in favor of such amendment, repeal, or addition, it shall be considered as adopted by the Officers.  Such amendments, repeals, or additions to these Bylaws shall be presented to the membership at the next general meeting where a quorum is present for Chapter ratification by a majority of the attending members.<br /><br />

							d. 	Amendments to these Bylaws shall become effective after Chapter ratification on the date specified by the Officers.
